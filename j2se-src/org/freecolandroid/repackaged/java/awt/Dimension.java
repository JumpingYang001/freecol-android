package org.freecolandroid.repackaged.java.awt;

public class Dimension {

	public int width;
	
	public int height;

	
	public Dimension(int width, int height) {
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

}
